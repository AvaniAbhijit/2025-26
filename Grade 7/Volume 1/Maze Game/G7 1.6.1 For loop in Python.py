# Loops help to repeat instructions multiple times until a specific condition is met.
# for loop used when you know how many times to repeat the instructions.
# Observe the indentation of code within the for loop.

# Task 1: Write a for loop to print numbers from 120 to 145.
# Task 2: Write a for loop to print the letters of your name.

# repeats 10 times from 1 to 10. Last number in range not included.
for num in range(1, 11):
    print(num, " in for loop")

# repeats based on number of letters in the string - Orchids
for letter in "Orchids":
    print(letter)
